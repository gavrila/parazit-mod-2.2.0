Process::mstime_t
Process::mstime()
{
    timeval tv;
    gettimeofday( &tv, 0 );
    return mstime_t( tv.tv_sec ) * mstime_t( 1000 ) + mstime_t( tv.tv_usec ) / mstime_t( 1000 );
}
