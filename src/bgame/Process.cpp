#include <bgame/impl.h>

//////////////////////////////////////////////////////////////////////////////

Process::Process()
    : _pendingReload   ( false )
    , _pendingShutdown ( false )
    , pendingReload    ( _pendingReload )
    , pendingShutdown  ( _pendingShutdown )
{
}

//////////////////////////////////////////////////////////////////////////////

Process::~Process()
{
}

//////////////////////////////////////////////////////////////////////////////

void
Process::init()
{
    signalInit();
}


//////////////////////////////////////////////////////////////////////////////

void
Process::setPendingReload( bool pending )
{
    _pendingReload = pending;
}

//////////////////////////////////////////////////////////////////////////////

void
Process::setPendingShutdown( bool pending )
{
    _pendingShutdown = pending;
}

//////////////////////////////////////////////////////////////////////////////

void
Process::shutdown()
{
    signalShutdown();
}

