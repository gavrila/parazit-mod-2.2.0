Process::mstime_t
Process::mstime()
{
    FILETIME ftime;
    GetSystemTimeAsFileTime( &ftime );

    ULARGE_INTEGER tmp;
    memcpy( &tmp, &ftime, sizeof(tmp) );

    return tmp.QuadPart / 10000;
}
